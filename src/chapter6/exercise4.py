# Write an invocation that counts the number of times the letter a occurs in “banana”.

stringName = 'banana'
subString = 'a'
stringCounter = stringName.count(subString)
print(f'The number of times the letter: \t {subString} \n'
      f'Appears in the word:\t{stringName} \n'
      f'Is: \t{stringCounter}')
